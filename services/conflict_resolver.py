def stream_synthesize_and_detect_conflicts(batch_raw_data: list):
    """
    统一使用流式呼叫大模型，兼顾 Map (终端直播初审) 和 Reduce (终端直播终审)
    """
    payload = {
        "model": "qwen3.6:27b",
        "prompt": generate_prompt(batch_raw_data),
        "stream": True,
        "options": {
            "temperature": 0.1,
            "num_ctx": 64836
        },
        "keep_alive": 0
    }

    try:
        response = requests.post(OLLAMA_TEXT_URL, json=payload, timeout=300, stream=True)

        if response.status_code != 200:
            yield f"❌ Ollama 请求异常，状态码: {response.status_code}\n"
            return

        for line in response.iter_lines():
            if line:
                chunk = json.loads(line.decode('utf-8'))
                text_fragment = chunk.get("response", "")
                if text_fragment:
                    yield text_fragment

    except Exception as e:
        yield f"\n❌ 流式生成发生中断: {str(e)}\n"